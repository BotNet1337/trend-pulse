#!/usr/bin/env node
// Sync ENV-variable tables in task docs with the Zod-validated config files
// that actually consume them. Lives under `.claude/` because it is Claude-Code
// tooling — not part of the backend build. The hook in
// `.claude/settings.local.json` calls this on PostToolUse Edit/Write/MultiEdit.
//
// How it works:
//   - `BLOCKS` below maps one task-doc to a list of `.config.ts` files.
//   - Each `.config.ts` exports a Zod schema; we regex-extract keys, min/default
//     modifiers, and optionality.
//   - In each doc we find the block between `<!-- env:<id>:start -->` and
//     `<!-- env:<id>:end -->` and replace its contents with a regenerated
//     markdown table + source reference. Prose outside the markers is left
//     alone, so a human can add context paragraphs above/below.
//
// Run manually:   node .claude/scripts/sync-task-env-docs.mjs
// Check only:     node .claude/scripts/sync-task-env-docs.mjs --check
//   (exits 1 if any block is stale — useful for CI.)
// Hook mode:      node .claude/scripts/sync-task-env-docs.mjs --post-tool-use
//   (silent when no changes — used by PostToolUse hook.)

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
// Script lives at `.claude/scripts/sync-task-env-docs.mjs` → two levels up = project root.
const PROJECT_ROOT = path.resolve(__dirname, '..', '..');

/**
 * @type {{ id: string, docPath: string, configPaths: string[], description: string, extraRows?: EnvRow[] }[]}
 */
// TrendPulse: define env-doc sync blocks here as the project grows.
// Each entry maps a task doc to the pydantic-settings/config files it documents.
const BLOCKS = [];

/**
 * @typedef {{ name: string, required: string, default: string, constraints: string, source?: string }} EnvRow
 */

/**
 * Extract env vars + modifiers from a Zod-schema source file.
 *
 * Recognises the top-level `z.object({ ... })` body and extracts
 * `NAME: z.<type>()...` entries plus `.min(N)`, `.default(V)`, `.optional()`,
 * and `.transform(...)` presence (we don't evaluate transforms, just note).
 *
 * @param {string} source
 * @returns {EnvRow[]}
 */
function extractEnvVars(source, sourceFile) {
  const objMatch = source.match(/z\.object\(\s*\{([\s\S]*?)\n\s*\}\s*\)/);
  if (!objMatch) return [];
  const body = objMatch[1];

  const results = [];
  // Match `NAME: <expr>,` where <expr> runs until end-of-line (may span lines via balanced parens/braces).
  const lineRegex = /^\s*([A-Z][A-Z0-9_]*)\s*:\s*(.+?),?\s*$/gm;
  let m;
  while ((m = lineRegex.exec(body))) {
    const name = m[1];
    const expr = m[2].trim();
    const minMatch = expr.match(/\.min\((\d+)\)/);
    const defaultMatch = expr.match(/\.default\(([^)]+)\)/);
    const optional =
      /\.optional\(\)/.test(expr) || defaultMatch !== null;
    const constraints = [
      minMatch ? `min length ${minMatch[1]}` : null,
      /\.email\(\)/.test(expr) ? 'email format' : null,
      /\.url\(\)/.test(expr) ? 'URL format' : null,
      /z\.union/.test(expr) ? 'union (see schema)' : null,
      /booleanLike/.test(expr) ? 'boolean (`true`/`false`/`1`/`0`)' : null,
    ]
      .filter(Boolean)
      .join('; ') || '—';
    results.push({
      name,
      required: optional ? 'optional' : '✅ required',
      default: defaultMatch ? `\`${defaultMatch[1].trim()}\`` : '—',
      constraints,
      source: sourceFile,
    });
  }
  return results;
}

/**
 * @param {EnvRow[]} vars
 * @param {string[]} sources
 * @param {string} description
 */
function renderBlock(vars, sources, description) {
  const header = '| Variable | Required | Default | Constraints |';
  const sep = '|---|---|---|---|';
  const rows = vars.map(
    (v) => `| \`${v.name}\` | ${v.required} | ${v.default} | ${v.constraints} |`,
  );
  const sourceList = sources.map((s) => `\`${s}\``).join(', ');
  return [
    `<!-- AUTO-GENERATED by .claude/scripts/sync-task-env-docs.mjs — do not edit by hand. -->`,
    `<!-- Run \`node .claude/scripts/sync-task-env-docs.mjs\` or let the Claude hook do it. -->`,
    '',
    `_${description}_`,
    '',
    `_Source: ${sourceList}_`,
    '',
    header,
    sep,
    ...rows,
  ].join('\n');
}

function syncBlock(block, opts) {
  const vars = [];
  for (const cfg of block.configPaths) {
    const abs = path.join(PROJECT_ROOT, cfg);
    if (!fs.existsSync(abs)) {
      console.warn(`[sync-env-docs] Config file not found: ${cfg}`);
      continue;
    }
    const src = fs.readFileSync(abs, 'utf8');
    vars.push(...extractEnvVars(src, cfg));
  }
  if (block.extraRows) {
    vars.push(...block.extraRows);
  }

  const startMarker = `<!-- env:${block.id}:start -->`;
  const endMarker = `<!-- env:${block.id}:end -->`;
  const docAbs = path.join(PROJECT_ROOT, block.docPath);
  if (!fs.existsSync(docAbs)) {
    console.warn(`[sync-env-docs] Doc not found: ${block.docPath}`);
    return { changed: false, stale: false };
  }
  const doc = fs.readFileSync(docAbs, 'utf8');
  const escapedStart = startMarker.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const escapedEnd = endMarker.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const pattern = new RegExp(`${escapedStart}[\\s\\S]*?${escapedEnd}`);
  if (!pattern.test(doc)) {
    console.warn(
      `[sync-env-docs] Markers '${startMarker}'..'${endMarker}' not found in ${block.docPath} — skipping.`,
    );
    return { changed: false, stale: false };
  }

  const body = renderBlock(vars, block.configPaths, block.description);
  const replacement = `${startMarker}\n${body}\n${endMarker}`;
  const next = doc.replace(pattern, replacement);
  if (next === doc) {
    return { changed: false, stale: false };
  }
  if (opts.check) {
    return { changed: false, stale: true };
  }
  fs.writeFileSync(docAbs, next);
  return { changed: true, stale: false };
}

function main() {
  const args = new Set(process.argv.slice(2));
  const check = args.has('--check');
  // In hook mode we only speak up when we actually changed something — otherwise
  // every Edit/Write in the repo prints noise.
  const quiet = args.has('--post-tool-use') || args.has('--quiet');
  let staleCount = 0;
  let changedCount = 0;
  for (const block of BLOCKS) {
    const { changed, stale } = syncBlock(block, { check });
    if (changed) {
      console.log(`[sync-env-docs] Updated ${block.docPath}`);
      changedCount++;
    }
    if (stale) {
      console.error(`[sync-env-docs] STALE: ${block.docPath} (run without --check to update)`);
      staleCount++;
    }
  }
  if (check && staleCount > 0) {
    process.exit(1);
  }
  if (!check && changedCount === 0 && !quiet) {
    console.log('[sync-env-docs] All ENV blocks already up to date.');
  }
}

main();
